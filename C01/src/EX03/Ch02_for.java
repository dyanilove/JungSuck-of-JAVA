package EX03;

public class Ch02_for {
	public static void main(String[] args) {
//		for문
//		: 조건을 만족하는 동안 블럭{}을 반복 - 반복횟수를 알 때 적합
		
		for(int i =0;i<10;i=i+2) {
			System.out.println("hello");
		}
		int sum =0;
		for(int i=0;i<5;i++) {
			sum += i;
			System.out.println(i);
		}
		
//		중첩 for문
		
	}

}
